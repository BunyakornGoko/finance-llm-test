@app.post("/chat")
async def chat(request: Request):
    """Chat endpoint ‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡∏ó‡∏∏‡∏Å‡∏´‡∏°‡∏ß‡∏î (debt/investment/savings/tax)"""
    data = await request.json()
    prompt = data.get("prompt", "")
    mode = data.get("mode", "default")  # üß≠ ‡πÄ‡∏û‡∏¥‡πà‡∏° parameter mode

    if not prompt:
        return JSONResponse({"error": "Missing 'prompt'"}, status_code=400)

    return StreamingResponse(stream_ollama(prompt, mode), media_type="text/plain")


@app.get("/finance/{symbol}")
async def get_stock(symbol: str):
    """‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏£‡∏≤‡∏Ñ‡∏≤‡∏´‡∏∏‡πâ‡∏ô‡∏à‡∏≤‡∏Å Yahoo Finance"""
    try:
        stock = yf.Ticker(symbol)
        info = stock.info
        return {
            "symbol": symbol.upper(),
            "price": info.get("currentPrice"),
            "previous_close": info.get("previousClose"),
            "market_cap": info.get("marketCap"),
            "currency": info.get("currency"),
            "short_name": info.get("shortName"),
        }
    except Exception as e:
        return JSONResponse({"error": str(e)}, status_code=500)


