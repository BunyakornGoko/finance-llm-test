def stream_ollama(prompt: str, mode: str = "default"):
    """‡∏™‡πà‡∏á prompt ‡πÑ‡∏õ‡∏¢‡∏±‡∏á Ollama ‡∏û‡∏£‡πâ‡∏≠‡∏° system prompt"""
    system_prompt = SYSTEM_PROMPTS.get(mode, SYSTEM_PROMPTS["default"])

    # ‡∏£‡∏ß‡∏° system prompt ‡πÄ‡∏Ç‡πâ‡∏≤‡∏Å‡∏±‡∏ö user prompt
    full_prompt = f"System: {system_prompt}\n\nUser: {prompt}\n\nAssistant:"

    payload = {
        "model": "llama3.2",
        "prompt": full_prompt,
        "stream": True,
        "temperature": 0.2,
        "top_p": 0.9,
        "top_k": 40,
        "repeat_penalty": 1.2,
        "num_predict": 250
    }

    start_time = time.time()

    with requests.post(OLLAMA_URL, json=payload, stream=True) as response:
        for line in response.iter_lines():
            if line:
                try:
                    data = json.loads(line.decode("utf-8"))
                    if "response" in data:
                        yield data["response"]
                    if data.get("done"):
                        break
                except Exception as e:
                    yield f"[Error parsing line: {e}]"

    duration = time.time() - start_time
    print(f"‚úÖ Ollama completed in {duration:.2f}s (mode: {mode})")


